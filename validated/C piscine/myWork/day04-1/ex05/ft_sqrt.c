int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb % 2 == 1)
		i++;
	while (((i * i) != nb) && (i <= (nb / 2)))
	{
		i = i + 2;
	}
	if ((i * i) == nb)
		return (i);
	return (0);
}
