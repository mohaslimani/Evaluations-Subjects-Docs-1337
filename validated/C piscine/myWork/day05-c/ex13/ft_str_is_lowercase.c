int		ft_str_is_lowercase(char *str)
{
	unsigned int i;

	i = 0;
	while (str[i] != '\0' && (str[i] <= 'z' && str[i] >= 'a'))
		i++;
	if (str[i] == '\0')
		return (1);
	return (0);
}
