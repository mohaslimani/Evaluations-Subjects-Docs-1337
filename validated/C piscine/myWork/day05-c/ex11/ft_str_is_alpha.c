int		ft_str_is_alpha(char *str)
{
	unsigned int i;

	i = 0;
	while (str[i] != '\0' && ((str[i] <= 'z' && str[i] >= 'a')
				|| (str[i] <= 'Z' && str[i] >= 'A')))
		i++;
	if (str[i] == '\0')
		return (1);
	return (0);
}
