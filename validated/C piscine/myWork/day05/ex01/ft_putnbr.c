void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	unsigned int n;

	if(nb < 0)
	{
		n = nb * -1;
		ft_putchar('-');
	}
	else
		n = nb;
	if(n > 9)
	{
		ft_putnbr(n / 10);
	}
	ft_putchar(n % 10 + '0');
}
